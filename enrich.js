// 通过卦名查 enrichment（做模糊匹配，因为 data.js 里的 name 格式可能是"乾为天"或其他）
function getEnrich(name){
  if(!name)return null;
  if(ENRICH[name])return ENRICH[name];
  // 模糊匹配：前两字
  var keys=Object.keys(ENRICH);
  for(var i=0;i<keys.length;i++){
    if(keys[i].indexOf(name.substr(0,2))===0)return ENRICH[keys[i]];
  }
  return null;
}
